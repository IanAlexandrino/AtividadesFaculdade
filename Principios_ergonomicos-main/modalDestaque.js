function defPreco(a) {
  var preco = document.querySelector("#preco");

  if (a == 1) {
    preco.innerHTML = "R$ 100,00";
  } else if (a == 2) {
    preco.innerHTML = "R$ 105,00";
  } else if (a == 3) {
    preco.innerHTML = "R$ 110,00";
  } else if (a == 4) {
    preco.innerHTML = "R$ 120,00";
  }
}
